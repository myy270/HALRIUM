//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitGame(void)
{
	// タイマーの初期化
	InitTimer();
	ResetTimer(13);


	InitLight();

	InitBasicScene();

	InitMouse();

	InitCancer();

	InitSagittarius();

	InitPisces();

	// BGM再生 ちゃんとloopできるにする!
	PlaySound(SOUND_LABEL_BGM000, XAUDIO2_LOOP_INFINITE);

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitGame(void)
{
	// タイマーの終了処理
	UninitTimer();

	UninitBasicScene();

	UnintMouse();

	UnintCancer();

	UnintSagittarius();

	UnintPisces();

	// BGM停止
	StopSound(SOUND_LABEL_BGM000);
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateGame(void)
{
	UpdateBasicScene();

	UpdateMouse();

	UpdateCancer();

	UpdateSagittarius();

	UpdatePisces();

	// タイマー処理の更新
	UpdateTimer();

}

